let nu1 = 20;
let nu2 = 150;

//operador de comparação igual ==
console.log(nu1 == nu2);

//operador de comparação diferente !=
console.log(nu1 != nu2);

//operador de comparação de valor e tipos ===
console.log(nu1 === nu2);

//operador de comparação de valor numerico do tipo > >= maior ou igual, < <= menor ou igual
console.log(nu1 > nu2); // falso

console.log(nu1 >= nu2); // falso

console.log(nu1 < nu2); // true

console.log(nu1 <= nu2); // true

